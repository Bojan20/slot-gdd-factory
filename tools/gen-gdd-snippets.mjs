#!/usr/bin/env node
/**
 * tools/gen-gdd-snippets.mjs
 *
 * Wave T4 — GDD snippet export per block.
 *
 * For each block in `src/blocks/<name>.mjs`:
 *   1. Statically import its `defaultConfig()` export.
 *   2. Pull the JSDoc header comment from the source (purpose lines).
 *   3. Pull the `GDD config (consumed from model.<key>):` section.
 *   4. Emit a Markdown snippet to `docs/gdd-snippets/<block>.md`:
 *        # <block> snippet
 *        > <purpose blurb>
 *        ```yaml
 *        <block>:
 *          enabled: true
 *          <key>: <default>
 *        ```
 *
 * Operator workflow:
 *   • Run once to refresh `docs/gdd-snippets/`.
 *   • Includes manifest at top (`docs/gdd-snippets/_index.md`) listing
 *     every block + 1-line summary.
 *   • Each snippet is copy-paste-able into a GDD's YAML frontmatter or
 *     inserted into a markdown GDD as a fenced code block.
 *
 * Senior-grade rule:
 *   • Single responsibility — snippet emit, nothing else.
 *   • 0 external deps (Node 22+ ESM + readFileSync + dynamic import).
 *   • Deterministic — same source → byte-identical output.
 *   • Vendor-neutral.
 */
import { readFileSync, readdirSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(__dirname, '..');
const BLOCKS_DIR = path.join(REPO, 'src', 'blocks');
const OUT_DIR    = path.join(REPO, 'docs', 'gdd-snippets');

const C = {
  green:  s => `\x1b[32m${s}\x1b[0m`,
  yellow: s => `\x1b[33m${s}\x1b[0m`,
  red:    s => `\x1b[31m${s}\x1b[0m`,
  cyan:   s => `\x1b[36m${s}\x1b[0m`,
  bold:   s => `\x1b[1m${s}\x1b[0m`,
  dim:    s => `\x1b[2m${s}\x1b[0m`,
};

/* Format a JS value as YAML scalar / inline-collection. Defensive — falls
 * back to JSON for nested/complex values so snippets stay valid YAML. */
function _yamlValue(v, indent = 2) {
  if (v === null) return 'null';
  if (typeof v === 'boolean') return String(v);
  if (typeof v === 'number') return Number.isFinite(v) ? String(v) : 'null';
  if (typeof v === 'string') {
    if (v === '' || /[:#&*!|>'"%@`{},\[\]]/.test(v) || /^\s|\s$/.test(v)) {
      return JSON.stringify(v);
    }
    return v;
  }
  if (Array.isArray(v)) {
    if (v.length === 0) return '[]';
    /* Inline arrays for primitive lists, else expand. */
    if (v.every(x => typeof x === 'number' || typeof x === 'string' || typeof x === 'boolean' || x === null)) {
      return JSON.stringify(v);
    }
    const pad = ' '.repeat(indent);
    return '\n' + v.map(x => `${pad}- ${_yamlValue(x, indent + 2).replace(/\n/g, '\n' + pad)}`).join('\n');
  }
  if (typeof v === 'object') {
    const keys = Object.keys(v);
    if (keys.length === 0) return '{}';
    const pad = ' '.repeat(indent);
    return '\n' + keys.map(k => {
      const child = _yamlValue(v[k], indent + 2);
      const sep = child.startsWith('\n') ? '' : ' ';
      return `${pad}${k}:${sep}${child}`;
    }).join('\n');
  }
  return JSON.stringify(v);
}

/* Extract the header JSDoc block (the leading slash-star-star comment
 * at the top of the file). First 2-3 sentences are the purpose blurb. */
function _purposeBlurb(src) {
  const m = src.match(/^\/\*\*([\s\S]*?)\*\//);
  if (!m) return '';
  const body = m[1]
    .replace(/^\s*\*\s?/gm, '')
    .replace(/^\s*src\/blocks\/[^\n]+/m, '')
    .trim();
  /* First paragraph after first blank line — typically the "Wave X / industry pattern" intro. */
  const para = body.split(/\n\s*\n/).find(p => p.length > 30 && !/^GDD config/i.test(p));
  if (!para) return '';
  /* Trim to ~2 sentences. */
  const cleaned = para.replace(/\s+/g, ' ').trim();
  const cut = cleaned.split(/(?<=[.!?])\s+(?=[A-Z])/).slice(0, 2).join(' ');
  return cut.length > 360 ? cut.slice(0, 360) + '…' : cut;
}

async function _loadDefaultConfig(file) {
  try {
    const mod = await import(pathToFileURL(file).href);
    if (typeof mod.defaultConfig === 'function') {
      const out = mod.defaultConfig();
      return (out && typeof out === 'object') ? out : null;
    }
  } catch (_) {}
  return null;
}

function _snippetForBlock(blockName, defaultCfg, purpose) {
  const yamlBody = _yamlValue({ [blockName]: defaultCfg }, 2).trimStart();
  return [
    `# ${blockName} — GDD snippet`,
    '',
    purpose ? `> ${purpose}` : '> (no purpose blurb extracted)',
    '',
    '```yaml',
    yamlBody,
    '```',
    '',
    '_Generated by `tools/gen-gdd-snippets.mjs`. Paste under the parser-',
    'recognised GDD section to override defaults per game._',
    '',
  ].join('\n');
}

function _indexSummary(rows) {
  const head = [
    '# GDD snippet index',
    '',
    '> Auto-generated from `src/blocks/<name>.mjs` `defaultConfig()`',
    '> exports + JSDoc header purpose lines.',
    '',
    `Total blocks: **${rows.length}**.`,
    '',
    '| Block | Has defaultConfig | Purpose |',
    '|:--|:-:|:--|',
  ];
  const body = rows.map(r => {
    const ok = r.hasDefault ? '✅' : '—';
    const purpose = (r.purpose || '').replace(/\|/g, '\\|').slice(0, 160);
    return `| \`${r.name}\` | ${ok} | ${purpose} |`;
  });
  return head.concat(body).join('\n') + '\n';
}

async function main() {
  if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });

  const files = readdirSync(BLOCKS_DIR).filter(f => f.endsWith('.mjs')).sort();
  let emitted = 0, skipped = 0;
  const rows = [];

  console.log(C.cyan(C.bold('\n📜 GDD snippet export — slot-gdd-factory\n')));
  console.log(C.dim(`   Scanning ${files.length} block(s) in src/blocks/`));
  console.log(C.dim(`   Writing to docs/gdd-snippets/\n`));

  for (const f of files) {
    const blockName = path.basename(f, '.mjs');
    const fullPath = path.join(BLOCKS_DIR, f);
    const src = readFileSync(fullPath, 'utf8');
    const purpose = _purposeBlurb(src);
    const defaultCfg = await _loadDefaultConfig(fullPath);

    rows.push({ name: blockName, hasDefault: !!defaultCfg, purpose });

    if (!defaultCfg) {
      console.log(`  ${C.yellow('—')} ${blockName} ${C.dim('(no defaultConfig export)')}`);
      skipped++;
      continue;
    }
    const md = _snippetForBlock(blockName, defaultCfg, purpose);
    writeFileSync(path.join(OUT_DIR, `${blockName}.md`), md);
    emitted++;
    if (emitted % 10 === 0) {
      console.log(C.dim(`  ${C.green('✓')} ${emitted} snippets so far…`));
    }
  }

  writeFileSync(path.join(OUT_DIR, '_index.md'), _indexSummary(rows));

  console.log(`\n  ${C.green('emitted:')} ${emitted}`);
  console.log(`  ${C.yellow('skipped:')} ${skipped} ${C.dim('(no defaultConfig export)')}`);
  console.log(`  ${C.cyan('index:')}   docs/gdd-snippets/_index.md\n`);
  console.log(C.green(C.bold(`✅ GDD snippet export complete.\n`)));
  process.exit(0);
}

main().catch((e) => {
  console.error(C.red(C.bold(`❌ snippet export failed: ${e.message}\n`)));
  process.exit(1);
});
