/* Canonical spin-lifecycle hooks. Density audit requires at least one
 * listener for each of these so the engine's contract is honored. */
const CANONICAL_LIFECYCLE_HOOKS = [
  'preSpin',
  'onSpinResult',
  'onTumbleStep',
  'postSpin',
  'onFsTrigger',
  'onFsSpinResult',
  'onFsEnd',
];

/* ─── 1. Audit each block ─────────────────────────────────────────────── */

const violations = [];

/* Block-level helpers. */
function loadBlockSrc(rel) {
  return readFileSync(joinPath(REPO_ROOT, rel), 'utf8');
}

/* Strip JSDoc / line / block comments so the probe doesn't fire on
 * HookBus.on('xxx' that lives inside documentation. We intentionally
 * KEEP template-string literals because SGF blocks emit their runtime
 * as template-strings — the listener calls inside those strings are
 * the actual listeners that ship at runtime (orchestrator concatenates
 * them into the page). Stripping templates would hide the truth. */
function stripCommentsAndTemplates(src) {
  let s = src;
  /* Block / JSDoc comments. */
  s = s.replace(/\/\*[\s\S]*?\*\//g, ' ');
  /* Line comments — careful not to chew through URL protocols like
   * `https://` or `file:///`. The leading char class blocks the most
   * common false strip while keeping the line-comment intent. */
  s = s.replace(/(^|[^:\\/])\/\/[^\n]*/g, '$1 ');
  return s;
}

/* Listener patterns: HookBus.on / HookBus.once / HookBus.waitFor all
 * count as the block declaring intent to subscribe to a hook. */
const LISTENER_PATTERN = /HookBus\.(?:on|once|waitFor)\(\s*['"]([a-zA-Z][a-zA-Z0-9_]*)['"]/g;
function hasListenerForHook(srcStripped, hook) {
  const safe = hook.replace(/[.[\]\\^$+*?()|{}]/g, '\\$&');
  const re = new RegExp(`HookBus\\.(?:on|once|waitFor)\\(\\s*['"]${safe}['"]`);
  return re.test(srcStripped);
}

/* Density: hook → Set of block files that listen on it. */
const listenerDensity = new Map();

for (const b of manifest.blocks) {
  const blockBase = b.file.split('/').pop();
  let srcRaw;
  try {
    srcRaw = loadBlockSrc(b.file);
  } catch (e) {
    violations.push({
      check: 'A0-source-load',
      block: blockBase,
      reason: `cannot read source file: ${e.message}`,
    });
    continue;
  }
  /* Strip comments + template literals BEFORE pattern matching so we
   * don't fire on doc examples / runtime emit string literals. */
  const src = stripCommentsAndTemplates(srcRaw);
  const claimed = Array.isArray(b.lifecycleHooks) ? b.lifecycleHooks.slice() : [];

  /* A1 — every claimed hook must appear as a listener call (on/once/waitFor).
   * Blocks in GRANDFATHERED_A1_OPTOUT (e.g. the bus implementation itself)
   * are skipped here. */
  if (!GRANDFATHERED_A1_OPTOUT.has(blockBase)) {
    for (const hook of claimed) {
      if (!hasListenerForHook(src, hook)) {
        violations.push({
          check: 'A1-manifest-source-parity',
          block: blockBase,
          hook,
          reason: `manifest claims listener for "${hook}" but source has no HookBus.on/once/waitFor('${hook}' call`,
        });
      }
    }
  }
  /* Aggregate density for A4 regardless of A1 opt-out (we still want
   * to know who listens to canonical hooks even if A1 is skipped). */
  for (const hook of claimed) {
    if (!listenerDensity.has(hook)) listenerDensity.set(hook, new Set());
    listenerDensity.get(hook).add(blockBase);
  }

  /* A3 — duplicate listener within the same block (same hook, ≥ 2 calls).
   * We use the same listener-pattern regex so on/once/waitFor each count;
   * the comment + template strip eliminates the JSDoc-example noise that
   * fired the first probe run. */
  LISTENER_PATTERN.lastIndex = 0;
  const perHookCount = new Map();
  let m;
  while ((m = LISTENER_PATTERN.exec(src))) {
    const h = m[1];
    perHookCount.set(h, (perHookCount.get(h) || 0) + 1);
  }
  for (const [h, n] of perHookCount.entries()) {
    if (n >= 2 && !GRANDFATHERED_DUPLICATES.has(`${blockBase}::${h}`)) {
      violations.push({
        check: 'A3-duplicate-listener-within-block',
        block: blockBase,
        hook: h,
        reason: `block subscribes to "${h}" ${n} times in its own source — likely copy-paste`,
      });
    }
  }
}

/* ─── 2. A2 — hook universe sanity ────────────────────────────────────── */

for (const [hook] of listenerDensity.entries()) {
  if (EXPECTED_EMIT_OWNERS_KEYS.has(hook)) continue;
  if (RUNTIME_GLUE_HOOKS.has(hook)) continue;
  if (GRANDFATHERED_DEAD_HOOKS.has(hook)) continue; /* tracked separately */
  /* Unknown hook = a listener that nobody emits → dead subscription. */
  violations.push({
    check: 'A2-unknown-hook',
    hook,
    reason: `hook "${hook}" has ≥ 1 listener but is not in EXPECTED_EMIT_OWNERS / RUNTIME_GLUE_HOOKS / GRANDFATHERED_DEAD_HOOKS — nobody emits it`,
  });
}

/* ─── 3. A4 — cross-block density sanity ──────────────────────────────── */

for (const hook of CANONICAL_LIFECYCLE_HOOKS) {
  const setForHook = listenerDensity.get(hook) || new Set();
  if (setForHook.size === 0) {
    violations.push({
      check: 'A4-zero-listener-density',
      hook,
      reason: `canonical lifecycle hook "${hook}" has ZERO listeners across the codebase`,
    });
  }
}

/* ─── 4. Report + exit ────────────────────────────────────────────────── */

const reportDir = joinPath(REPO_ROOT, 'reports');
if (!existsSync(reportDir)) mkdirSync(reportDir, { recursive: true });

const report = {
  generated: new Date().toISOString(),
  totals: {
    blocksScanned: manifest.blocks.length,
    distinctHooksHeard: listenerDensity.size,
    canonicalHooks: CANONICAL_LIFECYCLE_HOOKS.length,
    knownEmitOwners: EXPECTED_EMIT_OWNERS_KEYS.size,
    runtimeGlueHooks: RUNTIME_GLUE_HOOKS.size,
    violations: violations.length,
  },
  violations,
};
writeFileSync(joinPath(reportDir, 'lego-combination-probe.json'), JSON.stringify(report, null, 2));

console.log(`— LEGO combination probe (W61.A) —`);
console.log(`  blocks scanned        : ${manifest.blocks.length}`);
console.log(`  distinct hooks heard  : ${listenerDensity.size}`);
console.log(`  canonical hooks       : ${CANONICAL_LIFECYCLE_HOOKS.length} (all must have ≥ 1 listener)`);
console.log(`  known emit owners     : ${EXPECTED_EMIT_OWNERS_KEYS.size}`);
console.log(`  runtime-glue hooks    : ${RUNTIME_GLUE_HOOKS.size}`);
console.log(``);

if (violations.length === 0) {
  console.log(`  ✅ 0 violations across A1-A4 edges`);
  process.exit(0);
}

console.log(`  ❌ ${violations.length} violation(s):`);
for (const v of violations.slice(0, 50)) {
  const tail = [v.hook ? `hook=${v.hook}` : '', v.block ? `block=${v.block}` : ''].filter(Boolean).join(' ');
  console.log(`    [${v.check}] ${tail}`);
  console.log(`      ${v.reason}`);
}
if (violations.length > 50) {
  console.log(`    … (${violations.length - 50} more — see reports/lego-combination-probe.json)`);
}
process.exit(1);
