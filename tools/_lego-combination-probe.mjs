#!/usr/bin/env node
/**
 * tools/_lego-combination-probe.mjs — W61.A static LEGO combination probe.
 *
 * The existing LEGO gate (`tools/lego-gate.mjs`) enforces 7 invariants but
 * each one covers a single block in isolation: own-events single-owner,
 * at-least-one-listener, vendor-neutral source, etc. What it does NOT
 * verify is the EDGE matrix between blocks — the implicit emit ↔ listen
 * graph that ships at runtime. A silent drift in that graph (orphan
 * declaration in the manifest, listener that secretly disappeared from
 * source, duplicate listener within a single block) ships green but
 * breaks runtime in subtle ways.
 *
 * This probe scans the four edges of the LEGO graph and fails on drift:
 *
 *   A1. Manifest ↔ source listener parity
 *       For every block.lifecycleHooks entry in blocks/_manifest.json,
 *       assert the source file contains at least one matching
 *       `HookBus.on('<hook>'` call. The manifest is auto-generated by
 *       `gen-block-manifest.mjs` so a drift here means a refactor
 *       dropped a listener but the regen wasn't re-run.
 *
 *   A2. Hook universe sanity
 *       For every hook NAME that appears in any block's manifest entry,
 *       the hook must either be in EXPECTED_EMIT_OWNERS (someone emits it)
 *       OR be a known framework hook (onAutoplayTick fires from runtime
 *       glue, onBalanceChanged from balance HUD glue, etc). Unknown hook
 *       names = dead subscription = silent breakage.
 *
 *   A3. No duplicate listener within one block
 *       A single block subscribing to the same hook twice from the same
 *       source file is almost always a copy-paste accident. The runtime
 *       will fire the callback twice → counter double-count, etc.
 *
 *   A4. Cross-block listener density sanity
 *       For each canonical hook (preSpin, onSpinResult, onTumbleStep,
 *       postSpin, onFsTrigger, onFsSpinResult, onFsEnd), assert at
 *       least one listener exists across the codebase. A hook that is
 *       emitted but heard by ZERO blocks would mean nobody reacts to
 *       the spin lifecycle event — runtime would silently no-op past
 *       the engine's contract.
 *
 * Exit code 0 = full green. 1 = at least one edge tripped. The JSON
 * report at `reports/lego-combination-probe.json` always lists every
 * tripped edge with the block name + hook + reason so Boki can scrub.
 *
 * Vendor-neutral. Static analysis only — no Playwright, no DOM, no
 * runtime cost. Designed to run inside `npm run test:blocks` chain.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve as joinPath } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = joinPath(__dirname, '..');

/* ─── 0. Load manifest + lego-gate.mjs (for the EXPECTED_EMIT_OWNERS map) ─ */

const manifestPath = joinPath(REPO_ROOT, 'blocks/_manifest.json');
const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));

/* Parse EXPECTED_EMIT_OWNERS keys from lego-gate.mjs source. We just want
 * the set of canonical event names — owner mapping is not needed here. */
const legoSrc = readFileSync(joinPath(REPO_ROOT, 'tools/lego-gate.mjs'), 'utf8');
const EXPECTED_EMIT_OWNERS_KEYS = new Set();
{
  const re = /^\s*([A-Za-z][A-Za-z0-9]*)\s*:\s*\[/gm;
  const startIdx = legoSrc.indexOf('const EXPECTED_EMIT_OWNERS');
  if (startIdx < 0) {
    console.error('[probe] could not locate EXPECTED_EMIT_OWNERS block in lego-gate.mjs');
    process.exit(2);
  }
  /* Stop before the closing `};` so the regex doesn't bleed into other
   * blocks of the file (e.g. HOOK_REGISTRATION_OPT_OUT comments). */
  const endIdx = legoSrc.indexOf('};', startIdx);
  const sliced = legoSrc.slice(startIdx, endIdx > 0 ? endIdx : legoSrc.length);
  let m;
  while ((m = re.exec(sliced))) {
    EXPECTED_EMIT_OWNERS_KEYS.add(m[1]);
  }
}

